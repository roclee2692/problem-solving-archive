def solve():
    # 读取三个矩形的尺寸（已按题目要求排序）
    l1, b1, l2, b2, l3, b3 = map(int, input().split())
    
    # 情况1: 水平排列 - 三个矩形横向依次排列
    # 形成的矩形: (l1+l2+l3) × b1
    # 正方形条件: 所有矩形高度相同 且 总宽度 = 高度
    if b1 == b2 == b3 and l1 + l2 + l3 == b1:
        return "YES"
    
    # 情况2: 垂直排列 - 三个矩形纵向依次排列  
    # 形成的矩形: l1 × (b1+b2+b3)
    # 正方形条件: 所有矩形宽度相同 且 总高度 = 宽度
    if l1 == l2 == l3 and b1 + b2 + b3 == l1:
        return "YES"
    
    # 情况3: L型排列1 - 大矩形占上方，两小矩形占下方
    # 布局示意:
    # +-------------+
    # |    l1×b1    |  
    # +------+------+
    # |l2×b2 |l3×b3 |
    # +------+------+
    # 正方形条件: l1=l2+l3, b2=b3, b1+b2=l1
    if l1 == l2 + l3 and b2 == b3 and b1 + b2 == l1:
        return "YES"
    
    # 情况4: L型排列2 - 大矩形占左侧，两小矩形占右侧
    # 布局示意:
    # +-----+-----+
    # |     |l2×b2|
    # |l1×b1+-----+  
    # |     |l3×b3|
    # +-----+-----+
    # 正方形条件: b1=b2+b3, l2=l3, l1+l2=b1
    if b1 == b2 + b3 and l2 == l3 and l1 + l2 == b1:
        return "YES"
    
    return "NO"

# 主程序
t = int(input())
for _ in range(t):
    print(solve())